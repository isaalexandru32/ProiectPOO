    public void modList() {
        DefaultListModel model = new DefaultListModel();
        for (AlarmaSmart alarma : this.listaASMOD) {
            model.addElement(alarma.getNume());
        }
        this.jList1.setModel(model);
    }
    
    public void valueChanged(ListSelectionEvent e) {
        if(!e.getValueIsAdjusting() && this.jList1.getSelectedIndex() >= 0) {
                System.out.println(this.jList1.getSelectedIndex());
                this.jTextPane1.setText(Integer.toString(this.listaASMOD.get(this.jList1.getSelectedIndex()).getStoc()));
                this.jTextPane2.setText(Float.toString(this.listaASMOD.get(this.jList1.getSelectedIndex()).getPret()));
        }
    }

